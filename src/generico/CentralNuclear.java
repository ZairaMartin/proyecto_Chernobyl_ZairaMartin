	/*
	 * Retorna true si la celda NO es accesible (hay un edificio, un árbol, ...). El
	 * método es privado porque sólo se utiliza en la clase CentralNuclear
	 */
	private boolean celdaNoAccesible(int idCelda) {
		boolean noAccesible = true;
		int j = 0;
		while ((j < (Constantes.FILAS * Constantes.FILAS)) && (noAccesible)) {
			if (this.adyacencia[idCelda][j] == 1) {
				noAccesible = false;
			}
			j++;
		}
		return noAccesible;
	}

